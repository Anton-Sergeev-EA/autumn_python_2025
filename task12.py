# todo: Единицы массы пронумерованы следующим образом: 1 — килограмм,
#  2 — миллиграмм, 3 — грамм, 4 — тонна, 5 — центнер. Дан номер единицы массы
#  и масса тела M в этих единицах (вещественное число). Вывести массу данного
#  тела в килограммах

unit = int(input("Введите номер единицы массы (1-5): "))
mass = float(input("Введите массу: "))

# Коэффициенты перевода в килограммы
# 1 кг = 1 кг
# 1 мг = 0.000001 кг
# 1 г = 0.001 кг
# 1 т = 1000 кг
# 1 ц = 100 кг

if unit == 1:
    result = mass  # Килограммы
elif unit == 2:
    result = mass * 0.000001  # Миллиграммы
elif unit == 3:
    result = mass * 0.001  # Граммы
elif unit == 4:
    result = mass * 1000  # Тонны
elif unit == 5:
    result = mass * 100  # Центнеры
else:
    print("Ошибка: неверный номер единицы измерения")
    exit()

print(f"Масса в килограммах: {result} кг")