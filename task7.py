#todo: Даны три точки A , B , C на числовой оси. Найти длины отрезков AC и BC
# и их сумму. Примечание: все точки получаем через функцию input().

# Получаем координаты точек с помощью input()
a = float(input("Введите координату точки A, в мм.: "))
b = float(input("Введите координату точки B, в мм.: "))
c = float(input("Введите координату точки C, в мм.: "))

# Вычисляем длины отрезков
ac = abs(c - a)  # Расстояние между точками A и C
bc = abs(c - b)  # Расстояние между точками B и C

# Выводим результаты
print(f"Длина отрезка AC: {ac} мм.")
print(f"Длина отрезка BC: {bc} мм.")
print(f"Сумма длин отрезков AC и BC: {ac + bc} мм.")
