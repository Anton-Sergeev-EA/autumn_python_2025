# todo: Решить линейное уравнение A·x + B = 0, заданное своими коэффициентами
#  A и B (коэффициент A не равен 0). Примечание: коэффициенты получаем через
#  функцию input().

A = float(input("Введите коэффициент A: "))
B = float(input("Введите коэффициент B: "))

# Проверка условия (A не должно быть равно 0)
if A == 0:
    print("Ошибка: коэффициент A не может быть равен 0")
else:
    # Вычисление корня уравнения
    x = -B / A
    print(f"x = {x}")