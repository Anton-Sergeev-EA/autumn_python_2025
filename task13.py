# В восточном календаре принят 60-летний цикл, состоящий из 12- летних подциклов,
# обозначаемых названиями цвета: зеленый, красный, желтый, белый и черный.
# В каждом подцикле годы носят названия животных: крысы, коровы, тигра, зайца,
# дракона, змеи, лошади, овцы, обезьяны, курицы, собаки и свиньи. По номеру
# года вывести его название, если 1984 год был началом цикла — годом зеленой
# крысы.

def get_year_name(year):
    # Определяем базовые списки
    colors = ["зеленый", "красный", "желтый", "белый", "черный"]
    animals = ["крыса", "корова", "тигр", "заяц", "дракон",
               "змея", "лошадь", "овца", "обезьяна", "петух",
               "собака", "свинья"]
    
    # Вычисляем смещение от базового года (1984) для вычисления количество лет,
    # прошедших с начала цикла
    offset = year - 1984
    
    # Вычисляем индексы
    color_index = (offset // 12) % 5  # Цвет меняется каждые 12 лет
    # offset // 12 — делим на 12, чтобы узнать, сколько полных подциклов прошло
    # % 5 — берем остаток от деления на 5, так как цветов всего 5
    animal_index = offset % 12  # Животное меняется каждый год
    
    return f"{colors[color_index]} {animals[animal_index]}"


year = int(input("Введите год: "))
print(f"Год {year} — год {get_year_name(year)}")
