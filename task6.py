# Примечание: сторону квадрата получаем через функцию input().

def get_number(prompt):
    """Функция для безопасного ввода числа"""
    while True:
        try:
            # Сначала пытаемся преобразовать в float
            number = float(input(prompt))
            # Если число отрицательное, запрашиваем заново
            if number < 0:
                print("Ошибка! Длина стороны не может быть отрицательной.")
                continue
            # Если дробной части нет, возвращаем int
            if number.is_integer():
                return int(number)
            return number
        except ValueError:
            print("Ошибка! Введите корректное число.")


def main():
    # Получаем сторону квадрата с проверкой
    side = get_number("Введите длину стороны квадрата в мм.: ")
    
    # Вычисляем площадь
    area = side ** 2
    
    # Определяем формат вывода в зависимости от типа числа
    if isinstance(side, int):
        print(f"\nПлощадь квадрата со стороной {side} мм. равна {area} мм.кв.")
    else:
        print(f"\nПлощадь квадрата со стороной {side:.4f} мм. равна"
              f" {area:.4f} мм.кв.") # Форматированный вывод для дробных чисел с
        # округлением до 4 знаков.


if __name__ == "__main__":
    main()
